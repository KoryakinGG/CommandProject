package ru.rybinskov.ideas4transfer.dto;

public class BrandDto {
    private Long id;
    private String name;
    private String abbr;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAbbr() {
        return abbr;
    }

    public void setAbbr(String abbr) {
        this.abbr = abbr;
    }
}
